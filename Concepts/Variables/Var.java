package Concepts.Variables;

public class Var {
    public static void main(String[] args) {
        int v = 23; // Integer Variable

        System.out.println(v);

        char ch = 'A'; // Charecter Variable
        char c = (char)(ch+1);
        System.out.println(c);

        long lg = 1111111111;
        System.out.println(lg);



    }
}
